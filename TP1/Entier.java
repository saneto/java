import java.io.*;

public class Entier{
	int valeur;
	
	public Entier(int val){
		valeur = val;
	}
	
	public int factorielle(){
		int fact=1;
		for(int i=1; i<=valeur; i++)	fact *= i;
		return fact;
	}
	
	public void afficheValeur(){
		System.out.println("La valeur est : "+valeur);
	}
	
	public int somme(){
		int somme=0;
		for(int i=1; i<=valeur; i++)	somme += i;
		return somme;
	}
	
	public int puissance(int val){
		int puissance=1;
		for(int i=0; i<val; i++){
			puissance *= valeur;
		}
		return puissance;
	}
}
